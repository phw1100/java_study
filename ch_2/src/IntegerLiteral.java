public class IntegerLiteral {
    public static void main(StringEx[] args) {
        //2진수
        int var1 = 0b1011;
        //8진수
        int var2 = 0206;
        //10진수
        int var3 = 356;
        //16진수
        int var4 = 0xB3;

        System.out.println("var1: "+ var1);
        System.out.println("var2: "+ var2);
        System.out.println("var3: "+ var3);
        System.out.println("var4: "+ var4);

    }
}

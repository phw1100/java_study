public class VariableInitialization {
    public static void main(StringEx[] args) throws Exception {
        //변수 value 선언
        int value;
        //value 값 지정
        value = 1;

        //연산의 결과값을 result의 초기값으로 지정
        int result = value + 2;

        //result값을 읽고 콘솔에 출력
        System.out.println(result);
    
    }
}
